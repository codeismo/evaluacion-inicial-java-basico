package com.ferromex.examen.animales;

public class Vaca {
	/*Atributos*/
	boolean tomoHormonas;

	/*Constructor*/
	public Vaca(boolean tomoHormonas) {
		super();
		this.tomoHormonas = tomoHormonas;
	}
	
	/*Getters y Setters*/
	public boolean isTomoHormonas() {
		return tomoHormonas;
	}

	public void setTomoHormonas(boolean tomoHormonas) {
		this.tomoHormonas = tomoHormonas;
	}
	
}
